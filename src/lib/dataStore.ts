/**
 * Delete an entity by kind + id. Join-table edges cascade automatically via
 * the FK constraints; deleting a cell nulls its children's parentId (also via
 * the FK `on delete set null`).
 */
export async function deleteEntity(kind: EntityKind, id: string) {
  assertWritable();
  const { db, schema } = await import("@/db");
  const table = {
    cell: schema.cells,
    disease: schema.diseases,
    regimen: schema.regimens,
    drug: schema.drugs,
  }[kind];
  await db.delete(table).where(eq(table.id, id));
}

/** All existing IDs for a kind (used to generate collision-free new IDs). */
export async function existingIds(kind: EntityKind): Promise<string[]> {
  const { db, schema } = await import("@/db");
  const table = {
    cell: schema.cells,
    disease: schema.diseases,
    regimen: schema.regimens,
    drug: schema.drugs,
  }[kind];
  const rows = await db.select({ id: table.id }).from(table);
  return rows.map((r) => r.id);
}

